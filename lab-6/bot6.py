# –í–∞–ª–∏–¥–∞—Ü–∏—è –∫–æ–¥–∞ –≤–∞–ª—é—Ç—ã
def validate_currency(currency: str) -> bool:
    return bool(re.fullmatch(r'^[A-Za-z]{3}$', currency))

